// while and do while loops are used to execute a block of code repeatedly as long as a specified condition is true.

// The while loop checks the condition before executing the block of code, while the do-while loop executes the block of code at least once before checking the condition.

// Here is an example of a while loop:

let i = 0;
while(i < 5){
    console.log(i);
    i++;
}

// And here is an example of a do-while loop:

let j = 0;
do{
    console.log(j);
    j++;
}while(j < 5);

// In both examples, the loop will print the numbers from 0 to 4.